#!/usr/bin/env node
/**
 * One-off migration: rename existing translated blog-article files from
 * English slug filenames to localized slug filenames, and emit a mapping
 * suitable for generating vercel.json redirects.
 *
 * Usage:
 *   node scripts/migrate-blog-slugs-to-localized.js             # all drama-companion articles
 *   node scripts/migrate-blog-slugs-to-localized.js --dry-run   # preview without writing
 *   node scripts/migrate-blog-slugs-to-localized.js --lang id   # just one language
 *   node scripts/migrate-blog-slugs-to-localized.js --slug pursuit-of-jade-history-behind-the-drama
 *
 * Outputs:
 *   content/blog/slug-redirects.json  (mapping for redirect generation)
 *
 * After running:
 *   node scripts/generate-blog-slug-redirects.js   # writes redirects into vercel.json
 */

require('dotenv').config();
const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');
const OpenAI = require('openai');

const openai = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });

const BLOG_DIR = path.join(__dirname, '../content/blog');
const TRANSLATIONS_DIR = path.join(__dirname, '../content/blog/translations');
const REDIRECTS_JSON = path.join(__dirname, '../content/blog/slug-redirects.json');
const CONCURRENCY = parseInt(process.env.CONCURRENCY) || 10;

const LANGUAGES = {
  'es': 'Spanish', 'pt': 'Brazilian Portuguese', 'id': 'Indonesian',
  'vi': 'Vietnamese', 'ja': 'Japanese', 'ko': 'Korean', 'th': 'Thai',
  'hi': 'Hindi', 'ar': 'Arabic', 'fr': 'French', 'de': 'German',
  'tl': 'Filipino/Tagalog', 'ms': 'Malay', 'ru': 'Russian',
};

// Identify drama-companion article slugs (not idioms, not listicles).
// Idiom-page filenames look like pinyin slugs (short, no drama prefix).
// Drama prefixes:
const DRAMA_PREFIXES = [
  'pursuit-of-jade-',
  'love-beyond-the-grave-',
  'guardians-of-dafeng-',
  'first-frost-',
  'unveil-jadewind-',
  'rebirth-',
  'generation-to-generation-',
];
const DRAMA_EXACT = new Set([
  'learn-chinese-watching-pursuit-of-jade',
]);

function isDramaArticle(slug) {
  if (DRAMA_EXACT.has(slug)) return true;
  return DRAMA_PREFIXES.some(p => slug.startsWith(p));
}

function sanitizeSlug(raw, fallback) {
  if (!raw) return fallback;
  const clean = raw
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9-]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 80);
  return clean || fallback;
}

async function localizeSlug({ title, description, originalSlug, lang, langName }) {
  const prompt = `Generate a URL-safe localized slug for a blog article.

LANGUAGE: ${langName}
ORIGINAL ENGLISH SLUG: ${originalSlug}
TRANSLATED TITLE: ${title}
TRANSLATED DESCRIPTION: ${description}

RULES:
- Lowercase ASCII only: a-z, 0-9, hyphens. NO spaces, NO special chars, NO non-Latin scripts.
- For non-Latin-script languages (ja, ko, th, ar, ru, hi): romanize the translated title. Use Revised Romanization for Korean, Hepburn for Japanese, RTGS for Thai, GOST for Russian, Hunterian for Hindi, ISO 233 for Arabic.
- For Latin-script languages (es, pt, id, vi, fr, ms, tl, de): preserve base letters from accented (é→e, ñ→n).
- Keep brand anchors untranslated: "pursuit-of-jade", "love-beyond-the-grave", "first-frost", "rebirth", "guardians-of-dafeng", "generation-to-generation", "unveil-jadewind".
- Length: 40-80 characters. Descriptive but concise.
- MUST differ from the English original slug "${originalSlug}" (unless language is English).
- Should convey the article's topic in ${langName}.

EXAMPLES:
- English "pursuit-of-jade-history-behind-the-drama" → Indonesian "sejarah-di-balik-pursuit-of-jade"
- English "pursuit-of-jade-famous-quotes-chinese-explained" → Spanish "citas-famosas-de-pursuit-of-jade-explicadas-en-chino"
- English "love-beyond-the-grave-character-study-he-simu-duan-xu-idioms" → Korean "love-beyond-the-grave-he-simu-duan-xu-caracter-yeongu"

Return ONLY the slug string. No quotes. No explanation.`;

  const res = await openai.chat.completions.create({
    model: 'gpt-4o',
    messages: [{ role: 'user', content: prompt }],
    max_tokens: 150,
    temperature: 0.2,
  });
  const raw = res.choices[0].message.content.trim().split('\n')[0].trim();
  return sanitizeSlug(raw, originalSlug);
}

function parseArgs() {
  const args = process.argv.slice(2);
  return {
    dryRun: args.includes('--dry-run'),
    lang: args.includes('--lang') ? args[args.indexOf('--lang') + 1] : null,
    slug: args.includes('--slug') ? args[args.indexOf('--slug') + 1] : null,
  };
}

function loadRedirectsJson() {
  if (fs.existsSync(REDIRECTS_JSON)) {
    try { return JSON.parse(fs.readFileSync(REDIRECTS_JSON, 'utf-8')); } catch { /* fall through */ }
  }
  return { _comment: 'Generated by migrate-blog-slugs-to-localized.js. Maps originalSlug -> { lang: localizedSlug } for drama-companion blog articles.', articles: {} };
}

function saveRedirectsJson(data) {
  fs.writeFileSync(REDIRECTS_JSON, JSON.stringify(data, null, 2));
}

async function main() {
  const { dryRun, lang: langFilter, slug: slugFilter } = parseArgs();

  // Identify source drama articles
  const sourceFiles = fs.readdirSync(BLOG_DIR)
    .filter(f => f.endsWith('.md'))
    .map(f => f.replace(/\.md$/, ''))
    .filter(isDramaArticle);

  const slugs = slugFilter ? [slugFilter] : sourceFiles;
  const langs = langFilter ? { [langFilter]: LANGUAGES[langFilter] } : LANGUAGES;

  console.log(`${dryRun ? '[DRY RUN] ' : ''}Migrating ${slugs.length} article(s) × ${Object.keys(langs).length} language(s)`);
  if (dryRun) console.log('  (no files will be modified)');
  console.log('');

  const redirects = loadRedirectsJson();
  redirects.articles = redirects.articles || {};

  // Build work queue: files that still have English-slug filename in a translation dir
  const work = [];
  for (const originalSlug of slugs) {
    for (const [lang, langName] of Object.entries(langs)) {
      const langDir = path.join(TRANSLATIONS_DIR, lang);
      if (!fs.existsSync(langDir)) continue;
      const oldPath = path.join(langDir, `${originalSlug}.md`);
      if (!fs.existsSync(oldPath)) continue;

      // Skip if already migrated (some other file in this dir has originalSlug === this)
      const existing = fs.readdirSync(langDir).filter(f => f.endsWith('.md') && f !== `${originalSlug}.md`);
      let alreadyMigrated = false;
      for (const f of existing) {
        try {
          const { data } = matter(fs.readFileSync(path.join(langDir, f), 'utf-8'));
          if (data.originalSlug === originalSlug) { alreadyMigrated = true; break; }
        } catch { /* skip */ }
      }
      if (alreadyMigrated) continue;

      work.push({ originalSlug, lang, langName, oldPath });
    }
  }

  console.log(`${work.length} translation file(s) to migrate\n`);
  if (work.length === 0) { console.log('Nothing to do.'); return; }

  let done = 0, failed = 0;

  for (let i = 0; i < work.length; i += CONCURRENCY) {
    const batch = work.slice(i, i + CONCURRENCY);
    const results = await Promise.allSettled(batch.map(async (item) => {
      process.stdout.write(`[${done + 1}/${work.length}] ${item.lang}/${item.originalSlug}... `);
      const raw = fs.readFileSync(item.oldPath, 'utf-8');
      const { data: fm, content } = matter(raw);

      const localized = await localizeSlug({
        title: fm.title || item.originalSlug,
        description: fm.description || '',
        originalSlug: item.originalSlug,
        lang: item.lang,
        langName: item.langName,
      });

      // Guard: never let localized collide with an existing (different-article) file
      const targetPath = path.join(TRANSLATIONS_DIR, item.lang, `${localized}.md`);
      let finalLocalized = localized;
      if (fs.existsSync(targetPath) && targetPath !== item.oldPath) {
        // Suffix with -v2 etc. to avoid clobbering
        let n = 2;
        while (fs.existsSync(path.join(TRANSLATIONS_DIR, item.lang, `${localized}-v${n}.md`))) n++;
        finalLocalized = `${localized}-v${n}`;
      }

      if (dryRun) {
        console.log(`→ ${finalLocalized} [dry-run]`);
      } else {
        const newFm = { ...fm, originalSlug: item.originalSlug };
        const newOutput = matter.stringify(content, newFm);
        fs.writeFileSync(path.join(TRANSLATIONS_DIR, item.lang, `${finalLocalized}.md`), newOutput);
        fs.unlinkSync(item.oldPath);
        console.log(`→ ${finalLocalized}`);
      }

      // Record in redirects map
      redirects.articles[item.originalSlug] = redirects.articles[item.originalSlug] || {};
      redirects.articles[item.originalSlug][item.lang] = finalLocalized;

      done++;
    }));

    results.forEach((r, idx) => {
      if (r.status === 'rejected') {
        failed++;
        done++;
        console.log(`✗ ${batch[idx].lang}/${batch[idx].originalSlug}: ${r.reason.message}`);
      }
    });
  }

  if (!dryRun) saveRedirectsJson(redirects);

  console.log(`\n${done - failed} migrated, ${failed} failed.`);
  if (!dryRun) console.log(`Redirect map: ${REDIRECTS_JSON}`);
}

main().catch(err => { console.error(err); process.exit(1); });
